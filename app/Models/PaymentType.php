        {
            return new Exception("O campo tipo de pagamento Ã© obrigatÃ³rio.");
        }




        return parent::save($options); // TODO: Change the autogenerated stub
    }

    public function getContactTimeAttribute($value)
    {
        if ($value != "")
        {
            $value = CawHelpers::decodeData($value, true);
        }

        return $value;
    }

    public function getCreatedAtAttribute($value)
    {
        if ($value != "")
        {
            $value = CawHelpers::decodeData($value, true);
        }

        return $value;
    }

    public static function activeDisabled($id, $type){

        try {
            $payment_type = PaymentType::where('id', '=', $id)
                ->first();
            if($payment_type != null) {

                if ($type == 1) {
                    $payment_type->active = 0;
                } else {
                    $payment_type->active = 1;
                }

                $res = $payment_type->save();

                if($res === true){
                    return true;
                }else{
                    return false;
                }
            }else{
                return false;
            }

        }catch (QueryException $e){
            return false;
        }
    }

    public static function getSelect(){
        return PaymentType::where('active','=',1)->get();
    }

    public static function selectAll(){
        return PaymentType::groupBy('name','type')->orderBy('name')->get();
    }
}